#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	if (c == 0)
		return ((char *)&s[ft_strlen(s)]);
	else
	{
		i = 0;
		while (s[i] != '\0')
		{
			if (s[i] == (char)c)
				return ((char *)&s[i]);
			i++;
		}
		return (0);
	}
}
