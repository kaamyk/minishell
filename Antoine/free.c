#include "minishell.h"

void	free_ptr(void *ptr)
{
	if (ptr != NULL)
	{
		free(ptr);
		ptr = NULL;
	}
}

void	free_list(void **lst)
{
	size_t	i;

	if (lst != NULL)
	{
		i = 0;
		while (lst[i] != NULL)
		{
			free_ptr(lst[i]);
			++i;
		}
	}
	free(lst);
	lst = NULL;
}
